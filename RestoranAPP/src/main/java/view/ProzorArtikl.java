    public void napuniView() {
        var e = obrada.getEntitet();
        txtNaziv.setText(e.getNaziv());
        try {
            txtCijena.setText(df.format(e.getCijena()));
        } catch (Exception s) {
            txtCijena.setText("");
        }
        
        try {
            txtKategorija.setText(e.getKategorija());
        } catch (Exception s) {
            txtKategorija.setText("");
        }

        
    }
    
     public void ucitaj(){
     
         DefaultListModel<Artikl> m = new DefaultListModel<>();
         m.addAll(obrada.read(txtUvjet.getText()));
         lstPodaci.setModel(m);
         lstPodaci.repaint();
         
     }
    
    public void napuniModel(){
    
        var s = obrada.getEntitet();
        s.setNaziv(txtNaziv.getText());
        try {
            s.setCijena(BigDecimal.valueOf(df.parse(txtCijena.getText()).doubleValue()));
        } catch (Exception e) {
            s.setCijena(BigDecimal.ZERO);
        }
        
         try {
            s.setKategorija(txtKategorija.getText());
        } catch (Exception e) {
            txtKategorija.setText("");
        }
        
    
    }

