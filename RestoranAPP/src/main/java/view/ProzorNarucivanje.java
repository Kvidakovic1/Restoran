    private void napuniModel() {
            var v = obradan.getEntitet();
            
            if(txaNapomena.getText().isBlank() ){
                v.setNapomena("Nema napomene");
            }else{
                v.setNapomena(txaNapomena.getText());
            }
            v.setStol((Stol) cmbStolovi.getSelectedItem());
           
    }
